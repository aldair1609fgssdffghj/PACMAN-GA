public class Peso {

	/** ATRIBUTOS **/
	private float value;
	
	/** METODOS **/
	public Peso (float value){
		this.value = value;
	}//constructor
	
	/** GETTERS AND SETTERS **/
	public float getValue(){
		return value;
	}
	public void setValue(float value){
		this.value = value;
	}
}//class
